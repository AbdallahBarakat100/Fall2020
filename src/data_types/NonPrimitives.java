package data_types;

public class NonPrimitives {

    public static void main(String[] args) {
        String text = "My First String is super cool";
        String quote = "Better to be fool for a second than for whole life";
        System.out.println(text);
        System.out.println(quote);

        System.out.println(123);

        double myGpa = 4.0;

        System.out.println("My GPA: " + myGpa);

        int s = 36;
        System.out.println("Participants: " + s);

    }
}
